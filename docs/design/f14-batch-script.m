# F-14 バッチ台本生成設計

## 概要

現在は1スライド=1 APIリクエストのため、10枚PDFで10リクエスト消費。
Gemini無料枠（20 RPD）では1日2本しか作れない。
複数スライドを1リクエストにまとめることでAPIリクエスト数を削減する。

---

## 設計方針

- 複数スライド（2〜5枚）のテキストと画像を1リクエストにまとめて送信
- AIに「スライド1の台本:」「スライド2の台本:」のように区切って出力させる
- レスポンスをパースして各スライドの台本に分割
- バッチ生成が失敗した場合は1枚ずつの逐次生成にフォールバック

---

## バッチサイズとリクエスト数の比較

| バッチサイズ | 10枚PDFのリクエスト数 | Gemini無料枠(20RPD)で1日の本数 |
|------------|---------------------|-------------------------------|
| 1枚（変更前） | 10 | 2本 |
| 2枚 | 5 | 4本 |
| 3枚 | 4 | 5本 |
| 5枚 | 2 | 10本 |
| 全枚一括 | 1 | 20本 |

推奨バッチサイズ: 3〜5枚。全枚一括はスライド枚数が少ない場合のみ。

---

## 注意事項

- バッチサイズが大きすぎると出力品質が不安定になるリスクがある
- 画像を含む場合、1リクエストのトークン数が増大するためTPM制限に注意
- F-13（画像最適化）との併用でバッチ時のトークン消費を大幅に抑制可能

